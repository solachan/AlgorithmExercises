    public static int search(int[] arr, int target) {
        int left = 0,right = arr.length-1;
        while(left <= right){
            int mid = left + (right - left)/2;
            if(arr[left] == target){
                return left;
            }else if(arr[left] == arr[mid]){
                left++;
            }else if(arr[left] < arr[mid]){
                if(arr[left] > target || arr[mid] < target){
                    left = mid;
                }else {
                    left = left+1;
                    right = mid;
                }
            }else {
                if(arr[left] > target && arr[mid] < target){
                    left = mid;
                }else {
                    left = left+1;
                    right = mid;
                }
            }
        }
        return -1;
    }
